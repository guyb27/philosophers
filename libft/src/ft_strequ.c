#include "../include/libft.h"

int		ft_strequ(const char *s1, const char *s2)
{
	int i;

	i = 0;
	if (s1 && s2)
	{
		while (s1[i] == s2[i] && s1[i] && s2[i])
			i++;
		i = s1[i] - s2[i];
		if (i)
			return (0);
	}
	return (1);
}
